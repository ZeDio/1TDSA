'''
Fazer um algoritmo que lê um número e verifica se ele é negativo. Se for negativo, imprimir na tela a
mensagem "Numero negativo".
'''

#Entrada
numero = float(input("\nDigite o numero: "))

#Procesamento e Saida
if (numero >= 0):
    print(f"\nOlá, esse numero é positivo")
else:
    print(f"\nOlá, esse numero é negativo")